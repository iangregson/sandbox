function parseInput(_input) {
    _input = _input.split('\n')

    let [ l, s1, s2 ] = _input.shift().split(' ').map((i) => parseInt(i)),
        n = parseInt(_input.shift()),
        q = _input.map((i) => parseFloat(i))

    return { l, s1, s2, n, q }
}

